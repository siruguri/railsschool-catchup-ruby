# As we noted in the introduction, individual characters can be obtained using the [] operator, inside which you put
# a number, which is called the 'character index.'

str = "hello world"

# Note that indexing starts at 0
puts "The first character is "
puts str[0] # This prints 'h'

puts "The sixth character is "

# The index for the sixth character is 5.
puts str[5] # This appears to print nothing, because it's actually printing a space after the word 'is'

